package day15_forLoops;

public class C02_ForLoop2 {
    public static void main(String[] args) {

        // döngü sayısı i ile gösterilir ve döngü esnasında kullanılabilir.

        for (int i = 0; i <= 3; i++){
            System.out.println("i = " + i);
            
        }

        System.out.println("///////////////////////////////");

        for (int i =10; i<=20;i++){
            System.out.println("i = " + i);
        }
        System.out.println("////////////////////////////");
        for (int i = 0; i <= 10; i += 2){
            System.out.println("i = " + i);
        }
        System.out.println("//////////////////////////");

    }
}
